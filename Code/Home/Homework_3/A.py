"""
Вася изготовил прямоугольную полочку, отрезав от квадратного листа
фанеры две полосы — одну шириной a, другую высотой b.
Площадь оставшейся полочки равна S.
Необходимо определить длину стороны исходного квадратного листа фанеры L,
если известно, что она должна быть целым числом.
Если такого значения L не существует, следует вывести -1.
"""

a, b, S = map(int, input().split())

# Пусть исходный квадрат имел сторону L.
# После того как Вася отпилил a и b, площадь осталась:
# (L - a) * (L - b) = S
# => L² - L*(a + b) + a*b - S = 0
# => решаем квадратное уравнение относительно L

D = (a + b)**2 - 4 * (a * b - S)  # дискриминант

if D < 0:
    print(-1)
else:
    from math import sqrt, isclose
    L1 = ((a + b) + sqrt(D)) / 2
    L2 = ((a + b) - sqrt(D)) / 2

    # выбираем целое положительное решение
    L = -1
    for x in (L1, L2):
        if x > max(a, b) and isclose(x, round(x)) and x.is_integer():
            L = int(round(x))
            break

    print(L if L != -1 else -1)
